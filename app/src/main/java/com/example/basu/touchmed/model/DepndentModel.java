package com.example.basu.touchmed.model;

public class DepndentModel {

    private String name;

    private int image;

    public DepndentModel() {
    }

    public DepndentModel(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }
}

